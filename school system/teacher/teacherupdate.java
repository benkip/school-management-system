    private void btnsaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsaveMouseClicked
        // TODO add your handling code here:
        dbconnect db= new dbconnect();
        db.Connect();
        ResultSet rt;
        String fname,lname,sname,item;
        int id,x,y,sid;
        id=Integer.parseInt(txtid.getText());
        fname=txtfname.getText();
        lname=txtlname.getText();
        sname=txtsname.getText();
        db.qry="update teacher set f_name='"+fname+"',l_name='"+lname+"',s_name='"+sname+"', image='"+imgname+"' where teacher_id="+id;
        db.Insert(db.qry);
        y=lstselsub.getModel().getSize();
        x=0;
        db.qry="delete from teachersubject where teacher_id='"+id+"'";
        db.Insert(db.qry);
        while(x<y){
            item= (String) lstselsub.getModel().getElementAt(x);
            db.qry="select sub_id from subject where sub_name='"+item+"'";
            rt=db.queryString(db.qry);
            try {
                while(rt.next()){
                    sid=Integer.parseInt(rt.getString("sub_id"));
                    db.qry="insert into teachersubject (teacher_id,sub_id) values('"+id+"','"+sid+"')";
                    db.Insert(db.qry);
                }
            } catch (SQLException ex) {
                Logger.getLogger(teacheradd.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            x++;
        }
        this.dispose();
        //new teacherhome().setVisible(true);
    }//GEN-LAST:event_btnsaveMouseClicked


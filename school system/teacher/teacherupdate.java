    public void update() {
        String id,img;
        ResultSet rst;
        id=txtid.getText();
        dbconnect db=new dbconnect();
        db.Connect();
        db.qry="select * from teacher where teacher_id='"+id+"'";
        rst=db.queryString(db.qry);
        try {
            while(rst.next()) {
                txtfname.setText(rst.getString("f_name"));
                txtsname.setText(rst.getString("s_name"));
                txtlname.setText(rst.getString("l_name"));
                img=System.getProperty("user.dir")+"\\src\\images\\"+rst.getString("image");
                ImageIcon icon=new ImageIcon(img);
                lblimg.setIcon(icon);
            }
        } catch (SQLException ex) {
            Logger.getLogger(teacherupdate.class.getName()).log(Level.SEVERE, null, ex);
        }
        db.qry="select subject.sub_id,sub_name from subject, teachersubject where teacher_id='"+id+"' and subject.sub_id=teachersubject.sub_id";
        rst=db.queryString(db.qry);
        lstselsub.setModel(new DefaultListModel());
        DefaultListModel lm=(DefaultListModel) lstselsub.getModel();
        try {
            while(rst.next()){
                lm.addElement(rst.getString("sub_name"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(teacherupdate.class.getName()).log(Level.SEVERE, null, ex);
        }
        db.qry="select * from subject";
        rst=db.queryString(db.qry);
        lstsub.setModel(new DefaultListModel());
        lm=(DefaultListModel) lstsub.getModel();
        try {
            while(rst.next()){
                lm.addElement(rst.getString("sub_name"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(teacherupdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }   

